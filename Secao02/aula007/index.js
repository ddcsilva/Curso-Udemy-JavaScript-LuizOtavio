console.log("Danilo Silva"); // String = Texto (Utizando aspas duplas)
console.log('Danilo "Silva"'); // String = Texto (Utizando aspas simples)
console.log(`'Danilo' "Silva"`); // String = Texto (Utizando Template String)

console.log(123456); // Number = Número Inteiro
console.log(15.85); // Number = Número Decimal

console.log(35, 15.85, "Danilo Silva"); // Podemos imprimir mais de um valor na mesma linha